    def _dict_to_cypher_props(self, props, exclude_keys=None):
        """
        辅助函数：字典转Cypher属性
        :param props: 实体属性字典
        :param exclude_keys: 排除的键（不参与MERGE匹配）
        """
        if exclude_keys is None:
            exclude_keys = []
        props_str = []
        for k, v in props.items():
            if k in exclude_keys:
                continue  # 跳过排除的键
            if v is None:
                props_str.append(f"{k}: null")
            elif isinstance(v, str):
                props_str.append(f"{k}: '{v}'")
            elif isinstance(v, list):
                list_str = [f"'{item}'" if isinstance(item, str) else str(item) for item in v]
                props_str.append(f"{k}: [{', '.join(list_str)}]")
            else:
                props_str.append(f"{k}: {v}")
        return "{" + ", ".join(props_str) + "}"

    def _build_set_clause(self, props, exclude_keys=None):
        """构建SET子句，用于追加非匹配属性"""
        if exclude_keys is None:
            exclude_keys = []
        set_clauses = []
        for k, v in props.items():
            if k in exclude_keys:
                continue
            if v is None:
                set_clauses.append(f"e1.{k} = null")
            elif isinstance(v, str):
                set_clauses.append(f"e1.{k} = '{v}'")
            elif isinstance(v, list):
                list_str = [f"'{item}'" if isinstance(item, str) else str(item) for item in v]
                set_clauses.append(f"e1.{k} = [{', '.join(list_str)}]")
            else:
                set_clauses.append(f"e1.{k} = {v}")
        return "SET " + ", ".join(set_clauses) if set_clauses else ""

